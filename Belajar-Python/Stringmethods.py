# Belajar string method

Nama = "Fajar Zulmi Sopian"
Nama_count1 = Nama.count ('a')
print(Nama)
print(Nama.upper())#membuat semua huruf jadi kapital
print(Nama.capitalize())#Membuat huruf pertama jadi kapital
print(Nama.title())#Membuatt tiap huruf pada huruf pertama setiap kata jadi besar
print(Nama.casefold())
print(Nama.split(" "))#memotong tiap katta dan menjadi type data list
print(Nama.center(50))#memindahkan datta ke tengah
print("Jumlah a adalah ", Nama_count1)#fungsi count untuk menghitung jumlah huruf
